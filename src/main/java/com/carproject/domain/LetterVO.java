package com.carproject.domain;

public class LetterVO {
	private int l_id;
	private String m_id;
	private String to_id;
	private String title;
	private String content;
	private String w_date;
	private String r_date;
	private String m_show;
	private String to_show;
	
	
	public int getL_id() {
		return l_id;
	}
	public void setL_id(int l_id) {
		this.l_id = l_id;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getTo_id() {
		return to_id;
	}
	public void setTo_id(String to_id) {
		this.to_id = to_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getW_date() {
		return w_date;
	}
	public void setW_date(String w_date) {
		this.w_date = w_date;
	}
	public String getR_date() {
		return r_date;
	}
	public void setR_date(String r_date) {
		this.r_date = r_date;
	}
	public String getM_show() {
		return m_show;
	}
	public void setM_show(String m_show) {
		this.m_show = m_show;
	}
	public String getTo_show() {
		return to_show;
	}
	public void setTo_show(String to_show) {
		this.to_show = to_show;
	}
	

}
